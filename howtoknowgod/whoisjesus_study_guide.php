	<li>
		<p>According to Matthew 12:15:</p>
		<ol type="a"  class="answer_list" rel="Matthew 12:15">
			<li rel="correct">Yeshua healed crowds of people
			<span class="correct-answer">Correct</span>
			</li>
			<li rel="incorrect">while Yeshua did heal people here and there, (for example, see John 12:13), large numbers of people in need began following him
			<span class="incorrect-response">
			<strong>Incorrect:</strong> Yeshua healed crowds of people 
			</span>
			</li>
		</ol>
	</li>

	<li>
		<p>When Yeshua raised a man from the dead in the city of Nain, (Luke 7:11-17) he:</p>
		<ol type="a"  class="answer_list" rel="Luke 7:11-17">
			<li rel="correct">was showing compassion for the dead man's widowed mother 
			<span class="correct-answer">Correct</span>
			</li>
			<li  rel="incorrect">scared the onlookers away
			<span class="incorrect-response">
			<strong>Incorrect:</strong> in verse 16 the &quot;fear&quot; that they experienced was reverent awe, but they were happy that God was among them in a special way 
			</span>
			</li>
		</ol>
	</li>

	<li>
		<p>In Matthew 9:1-8, when Yeshua told a paralyzed man that his sins were forgiven, some of the religious leaders who heard him:</p>
		<ol type="a"  class="answer_list" rel="Matthew 9:1-8">
			<li rel="incorrect">told the man he should be thankful to Yeshua
			<span class="incorrect-response">
			<strong>Incorrect:</strong> there is no record of religious leaders saying anything in this narrative</span>
			</li>
			<li rel="correct">thought to themselves that Yeshua had committed a terrible crime
			<span class="correct-answer">Correct: they thought Yeshua was blasphemous because he claimed to do something only God can do (see verse 3)</span>
			</li>
		</ol>
	</li>

	<li>
		<p>In John 3:2 a man named Nicodemus acknowledged Yeshua as a teacher who  came from God because:</p>
		<ol type="a"  class="answer_list" rel="John 3:2">
			<li  rel="correct">Nicodemus understood that the kind of miracles Yeshua performed were signs that God was with him
			<span class="correct-answer">Correct</span>
			</li>
			<li  rel="incorrect">Nicodemus was a gullible, uneducated person
			<span class="incorrect-response"><strong>Incorrect:</strong> Nicodemus was a Pharisee, and a member of the Jewish ruling council, (see verse 1)</span>
			</li>
		</ol>
	</li>

	<li>
	<p>While celebrating Hanukkah at the Temple in Jerusalem, Yeshua said, &quot;I and the Father are one&quot; (John 10:25-30) because:</p>
	<ol type="a"  class="answer_list" rel="Luke 1:37">
		<li  rel="incorrect">the religious leaders had just asked how devoted he was to God
		<span class="incorrect-response"><strong>Incorrect:</strong> in verse 24 they had just asked whether or not he was the Messiah.</span>
		</li>
		<li  rel="correct">he was responding to the question of his identity by telling why he was able to do things that only God could do
		<span class="correct-answer">Correct</span>
		</li>
	</ol>
	</li>
	

	<li>
	<p>When Yeshua said, &quot;Anyone who has seen me has seen the Father&quot; (John 14:9) he was: </p>
	<ol type="a"  class="answer_list" rel="John 14:9">
		<li  rel="correct">responding to a sincere request to see God
		<span class="correct-answer">Correct</span>
		</li>
		<li  rel="incorrect">telling the religious leaders to look at him more closely 
		<span class="incorrect-response"><strong>Incorrect:</strong> he was talking to one of his own disciples who still did not realize who he was</span>
		</li>
	</ol>
	</li>

	<li>
	<p>When Yeshua told his disciples he was going to his father’s house to prepare a place for them, (John 14:2-3) the point was:</p>
	<ol type="a"  class="answer_list" rel="John 14:2-3">
		<li  rel="incorrect">the disciples were tired of traveling so Yeshua was going to make room for them in the house where he grew up
		<span class="incorrect-response"><strong>Incorrect:</strong> he was not speaking of his mother's husband's house, but of God’s &quot;house.&quot;</span>
		</li>
		<li  rel="correct">Yeshua was leaving his disciples for a good reason, and one day he would return to bring them to his heavenly home
		<span class="correct-answer">Correct</span>
		</li>
	</ol>
	</li>

	<li>
	<p>When Yeshua was tried before the Sanhedrin, (Mark 14:60-64) he was condemned and sentenced to death for:</p>
	<ol type="a"  class="answer_list" rel="Mark 14:60-64">
		<li  rel="incorrect">claiming to be the Messiah
		<span class="incorrect-response"><strong>Incorrect:</strong> while he did claim to be the Messiah in this passage, that in itself was not enough to sentence him to death</span>
		</li>
		<li  rel="correct">claiming to be divine
		<span class="correct-answer">Correct: Yeshua told the court that he was the Messiah, but he also predicted that he would sit at the right hand of the Power (God) 
		and return in the clouds of heaven. They realized he was claiming equality 
		with God, and since they did not believe him, they condemned him for blasphemy</span>
		</li>
	</ol>
	</li>
	
	
	<li>
		<p>When King David wrote, &quot;My God, My God why have you forsaken me&quot; in Psalm 22:14-18 he was</p>
		<ol type="a" class="answer_list" rel="Psalm 22:14-18">
			<li rel="correct">predicting the suffering and death of the Messiah, Yeshua 
			<span class="correct-answer">Correct: see Matthew 27:35</span>
			</li>		
			<li  rel="incorrect">dying at the hand of his enemies
			<span class="incorrect-response"><strong>Incorrect:</strong> King David was being pursued by his enemies but he did not die until much later, and not in the way described here</span>
			</li>
		</ol>
	</li>

