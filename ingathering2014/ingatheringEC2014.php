<p><strong>Join us for Ingathering 2014, a weekend away in the Poconos.</strong></p>
<p>Get quiet with God, and get loud together.</p>
<p>Celebrate with other Jewish believers in Messiah:</p>
<ul>
  <li>Family</li>
